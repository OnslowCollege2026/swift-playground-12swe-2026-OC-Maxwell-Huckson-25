/// Calculates the area of a room.
/// - Parameters:
///   - Length: The rooms length is metres.
///   - Width: The rooms width is metres.
/// - Returns: The area of the room in metres.
func roomArea(length: Double, width: Double) -> Double {
    return length * width
}

